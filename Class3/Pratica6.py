# Expressões booleanas
# Escreva as seguintes expressões booleanas # em linguagem Python

print("a) O somatório de 2 com 2 é menor que 4")
result = 2 + 2 < 4
print(result)

print("b) O valor 7 // 3 é igual a 1+1")
result = 7 // 3 == 1 + 1
print(result)

print("c) A soma de 3 elevado ao quadrado com 4 elevado ao quadrado é igual a 25")
result = 3**2 + 4**2 == 25
print(result)

print("d) A soma de 2, 4 e 6 é maior que 12")
